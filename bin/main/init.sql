-- 오늘의 BRIEFING 생성 + BRIEFING_SUMMARY 매핑
WITH general_ids AS (
    SELECT id FROM NEWS_SUMMARY
    WHERE SUMMARY_TYPE = 'GENERAL' AND TARGET_DATE = CURRENT_DATE
    ORDER BY ID
),
new_briefing AS (
    INSERT INTO NEWS_SUMMARY (SUMMARY_TYPE, TITLE, SUMMARY_CONTENT, INSIGHT, AI_MODEL, TARGET_DATE, STATUS_CODE)
    VALUES (
        'BRIEFING',
        '오늘의 3분 브리핑 - ' || TO_CHAR(CURRENT_DATE, 'YYYY년 MM월 DD일'),
        'AI 혁신 물결과 증시 반등이 오늘의 핵심 이슈입니다.',
        '기술 혁신과 금융시장 회복이 동시에 진행되는 흐름 속에서 관련 섹터의 투자 기회를 주목해야 할 시점입니다.',
        'gpt-4o', CURRENT_DATE, 'PUBLISHED'
    )
    RETURNING id
)
INSERT INTO BRIEFING_SUMMARY (BRIEFING_ID, SUMMARY_ID)
SELECT nb.id, gi.id
FROM new_briefing nb
CROSS JOIN general_ids gi;

