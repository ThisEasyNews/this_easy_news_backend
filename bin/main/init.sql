-- ============================================================
-- This Easy News  DB 초기화 (PostgreSQL)
-- ============================================================

-- 1. 그룹 코드
CREATE TABLE IF NOT EXISTS COMMON_GROUP (
    ID          VARCHAR(50)  PRIMARY KEY,
    NAME        VARCHAR(50)  NOT NULL,
    CREATED_AT  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    STATUS_CODE VARCHAR(50)  NOT NULL DEFAULT 'PUBLISHED',
    START_DATE  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_DATE    TIMESTAMP    NOT NULL DEFAULT '9999-12-31 23:59:59'
);

-- 2. 세부 항목
CREATE TABLE IF NOT EXISTS COMMON_DETAIL (
    ID          VARCHAR(50)  PRIMARY KEY,
    GROUP_ID    VARCHAR(50)  NOT NULL,
    NAME        VARCHAR(50)  NOT NULL,
    CODE_VALUE  VARCHAR(5000),
    CREATED_AT  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    STATUS_CODE VARCHAR(50)  NOT NULL DEFAULT 'PUBLISHED',
    START_DATE  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_DATE    TIMESTAMP    NOT NULL DEFAULT '9999-12-31 23:59:59',
    CONSTRAINT fk_detail_group FOREIGN KEY (GROUP_ID) REFERENCES COMMON_GROUP(ID)
);

-- 3. 뉴스 요약 (GENERAL / BRIEFING 통합)
CREATE TABLE IF NOT EXISTS NEWS_SUMMARY (
    ID              SERIAL        PRIMARY KEY,
    SUMMARY_TYPE    VARCHAR(50)   NOT NULL DEFAULT 'GENERAL',
    TITLE           VARCHAR(1000) NOT NULL,
    SUMMARY_CONTENT TEXT          NOT NULL,
    INSIGHT         TEXT,
    AI_MODEL        VARCHAR(50),
    TOP_IMAGE_URL   VARCHAR(5000),
    VIEW_COUNT      INT           DEFAULT 0,
    TARGET_DATE     DATE,
    CREATED_AT      TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT      TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    STATUS_CODE     VARCHAR(50)   NOT NULL DEFAULT 'PUBLISHED',
    START_DATE      TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_DATE        TIMESTAMP     NOT NULL DEFAULT '9999-12-31 23:59:59'
);

-- 4. 기사
CREATE TABLE IF NOT EXISTS ARTICLE (
    ID             SERIAL        PRIMARY KEY,
    SUMMARY_ID     INT,
    MEDIA_ID       VARCHAR(50),
    CATEGORY_ID    VARCHAR(50),
    ORIGINAL_TITLE VARCHAR(1000) NOT NULL,
    URL            VARCHAR(5000) NOT NULL UNIQUE,
    CONTENT        TEXT,
    PUBLISHED_AT   TIMESTAMP,
    SCRAPED_AT     TIMESTAMP,
    IS_SUMMARIZED  BOOLEAN       DEFAULT FALSE,
    CREATED_AT     TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT     TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    STATUS_CODE    VARCHAR(50)   NOT NULL DEFAULT 'PUBLISHED',
    START_DATE     TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_DATE       TIMESTAMP     NOT NULL DEFAULT '9999-12-31 23:59:59',
    CONSTRAINT fk_article_summary  FOREIGN KEY (SUMMARY_ID)  REFERENCES NEWS_SUMMARY(ID),
    CONSTRAINT fk_article_media    FOREIGN KEY (MEDIA_ID)    REFERENCES COMMON_DETAIL(ID),
    CONSTRAINT fk_article_category FOREIGN KEY (CATEGORY_ID) REFERENCES COMMON_DETAIL(ID)
);

-- 5. 키워드 마스터
CREATE TABLE IF NOT EXISTS NEWS_KEYWORD (
    ID          VARCHAR(50) PRIMARY KEY,
    KEYWORD     VARCHAR(50) NOT NULL UNIQUE,
    CREATED_AT  TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    STATUS_CODE VARCHAR(50) NOT NULL DEFAULT 'PUBLISHED',
    START_DATE  TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_DATE    TIMESTAMP   NOT NULL DEFAULT '9999-12-31 23:59:59'
);

-- 6. 요약-키워드 매핑
CREATE TABLE IF NOT EXISTS SUMMARY_KEYWORD (
    ID          SERIAL      PRIMARY KEY,
    SUMMARY_ID  INT         NOT NULL,
    KEYWORD_ID  VARCHAR(50) NOT NULL,
    CREATED_AT  TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_sk_summary FOREIGN KEY (SUMMARY_ID) REFERENCES NEWS_SUMMARY(ID)  ON DELETE CASCADE,
    CONSTRAINT fk_sk_keyword FOREIGN KEY (KEYWORD_ID) REFERENCES NEWS_KEYWORD(ID)  ON DELETE CASCADE
);

-- 7. 브리핑-뉴스요약 매핑
CREATE TABLE IF NOT EXISTS BRIEFING_SUMMARY (
    ID          SERIAL  PRIMARY KEY,
    BRIEFING_ID INT     NOT NULL,
    SUMMARY_ID  INT     NOT NULL,
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bs_briefing FOREIGN KEY (BRIEFING_ID) REFERENCES NEWS_SUMMARY(ID) ON DELETE CASCADE,
    CONSTRAINT fk_bs_summary  FOREIGN KEY (SUMMARY_ID)  REFERENCES NEWS_SUMMARY(ID) ON DELETE CASCADE
);

-- 8. 키워드 일별 통계
CREATE TABLE IF NOT EXISTS KEYWORD_LOG (
    ID            SERIAL      PRIMARY KEY,
    KEYWORD_ID    VARCHAR(50) NOT NULL,
    TARGET_DATE   DATE        NOT NULL,
    MENTION_COUNT INT         DEFAULT 1,
    CREATED_AT    TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT    TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_log_keyword  FOREIGN KEY (KEYWORD_ID) REFERENCES NEWS_KEYWORD(ID) ON DELETE CASCADE,
    CONSTRAINT uq_keyword_date UNIQUE (KEYWORD_ID, TARGET_DATE)
);

-- 9. 배치 실행 로그
CREATE TABLE IF NOT EXISTS BATCH_LOG (
    ID                SERIAL       PRIMARY KEY,
    JOB_NAME          VARCHAR(100) NOT NULL,
    START_TIME        TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    END_TIME          TIMESTAMP,
    STATUS_CODE       VARCHAR(20)  NOT NULL,
    TOTAL_COUNT       INT          DEFAULT 0,
    SUCCESS_COUNT     INT          DEFAULT 0,
    FAIL_COUNT        INT          DEFAULT 0,
    ERROR_MESSAGE     TEXT,
    EXECUTION_TIME_SEC INT,
    CREATED_AT        TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT        TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);

-- 인덱스
CREATE INDEX IF NOT EXISTS idx_summary_type_date  ON NEWS_SUMMARY(SUMMARY_TYPE, TARGET_DATE);
CREATE INDEX IF NOT EXISTS idx_article_published  ON ARTICLE(PUBLISHED_AT DESC);
CREATE INDEX IF NOT EXISTS idx_article_media      ON ARTICLE(MEDIA_ID);
CREATE INDEX IF NOT EXISTS idx_article_category   ON ARTICLE(CATEGORY_ID);
CREATE INDEX IF NOT EXISTS idx_keyword_log_date   ON KEYWORD_LOG(TARGET_DATE, MENTION_COUNT DESC);

